    {
        $comment = new Comentario;
        $comment->user_debate = $request->userComentario;
        $comment->contenido = $request->comentario;
        $comment->debate_id = $request->debate_id;
        $comment->save();
        $id = request()->debate_id;
        return redirect()->route('debates.show', compact('id'));
    }

